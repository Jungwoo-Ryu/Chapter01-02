package com.ohgiraffers.section01.literal;

public class Application03 {
    public static void main(String[] args) {
        // Goal. Applying concatenation
        // Chapter. 1. concatenating two Strings
        System.out.println("--------Concatenating two Strings----------");
        System.out.println(9+9);
        System.out.println("9"+9);
        System.out.println(9+"9");
        System.out.println("9"+"9");

        // Chapter 2. concatenating three Strings
        System.out.println("--------Concatenating three Strings----------");
        System.out.println(9+9+"9");
        System.out.println(9+"9"+9);
        System.out.println("9"+(9+9));

        System.out.println("-------print the sum of 10 and 20------");
        System.out.println("The sum of 10 and 20: " + (10 + 20));
    }


}
