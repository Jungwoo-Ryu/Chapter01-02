package com.ohgiraffers.section02.variable;

public class Application02 {
    public static void main(String[] args) {
        // Goal. Initializing variable

        //Chapter. 1. prepare variable
        //Chapter. 1-1. initializing variable
        //Chapter. 1-1-1. number types
        byte bnum; // 1 byte
        short snum; // 2 byte
        int inum; // 4 byte
        long lnum; // 8 byte

        float fnum; // 4 byte
        double dnum; // 8 byte

        //Chapter. 1-1-2. Character types
        char ch; // 2 byte
        char ch2;

        //Chapter. 1-1-3/ Boolean types
        boolean isTrue; // 1 byte

        //Chapter/ 1-1-4. String types
        String str;  // 4 byte

        //Chapter. 2. Substituting values
        //Chapter. 2-1. Integer
        bnum = 1;
        snum = 2;
        inum = 4;
        lnum = 8L;

        //Chapter. 2-2. Real number
        fnum = 4.0f;
        dnum = 8.0;

        // Chapter. 2-3. Character
        ch = 'a';
        ch = 97;

        // Chapter. 2-4. Boolean
        isTrue = true;
        isTrue = false;

        // Chapter. 2-5. String
        str = "Hello";
    }
}